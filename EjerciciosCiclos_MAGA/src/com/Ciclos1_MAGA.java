package com;

public class Ciclos1_MAGA {

	public static void main(String[] args) {
		/*
		 * EJERCICIO 1:
		 * Programa un algoritmo que realice la tabla de multiplicar del 12
		 * */
		
		int i = 1;

		System.out.println("Tabla de multiplicar del 12\n");
		while (i <= 12) {
			System.out.println(i + " X 12 = " + (i * 12));
			i++;
		}
	}

}
