package com;

public class Ciclos1_OBA {

	public static void main(String[] args) {   //tabla de multplicar del 12
		
	      int numero = 12;

	        System.out.println("Tabla de multiplicar del " + numero + ":");

	        for (int i = 1; i <= 10; i++) {
	            int resultado = numero * i;
	            System.out.println(numero + " x " + i + " = " + resultado);
	        }
		
		

	}

}
